char	*ft_strcpy(char *dest, char *src)
{
	int		i;
	char	*dst;

	dst = dest;
	i = 0;
	while (*src != '\0')
	{
		*dst = *src;
		dst++;
		src++;
	}
	*dst = '\0';
	return (dest);
}
